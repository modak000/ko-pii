# Suffix 변형 — "컴퓨터공학과" "컴퓨터공학부" "컴퓨터공학전공" → "컴퓨터공학"
_SUFFIX_VARIANTS = ("학과", "학부", "전공", "과")


def normalize_major(token: str) -> str:
    """접미사 제거 + 표준 명칭으로 정규화.

    "컴퓨터공학과" → "컴퓨터공학"
    "경영학부" → "경영학"
    "법학전공" → "법학"
    "컴공과" → "컴퓨터공학" (약칭 사전 매핑)
    """
    # 정확 매칭 우선
    if token in ALL_MAJORS:
        return token
    # 약칭 매핑 (정확 일치)
    if token in MAJOR_ABBREV:
        return MAJOR_ABBREV[token]
    # 접미사 제거 시도
    for suf in _SUFFIX_VARIANTS:
        if token.endswith(suf) and len(token) > len(suf):
            stem = token[: -len(suf)]
            # "학" 으로 끝나면 그대로 (예: "컴퓨터공학과" → "컴퓨터공학")
            if stem in ALL_MAJORS:
                return stem
            # "학" 안 붙은 경우 "학" 추가 시도 (예: "경영과" → "경영학")
            if (stem + "학") in ALL_MAJORS:
                return stem + "학"
    return token


def is_major(token: str) -> bool:
    """전공명 인식 (접미사 변형 + 약칭 포함)."""
    if token in MAJOR_ABBREV:
        return True
    return normalize_major(token) in ALL_MAJORS
