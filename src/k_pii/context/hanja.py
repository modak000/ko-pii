def hanja_to_hangul(text: str) -> str:
    """Best-effort 변환. 매핑 없는 한자는 원본 보존.

    >>> hanja_to_hangul("洪吉童")
    '홍길동'
    >>> hanja_to_hangul("南宮民秀")
    '남궁민수'
    """
    if not text:
        return text
    out: list[str] = []
    i = 0
    n = len(text)
    while i < n:
        # 2자 복성 우선 매칭
        if i + 1 < n and text[i:i+2] in _SURNAME_HANJA:
            out.append(_SURNAME_HANJA[text[i:i+2]])
            i += 2
            continue
        ch = text[i]
        if ch in _SURNAME_HANJA:
            out.append(_SURNAME_HANJA[ch])
        elif ch in _GIVEN_HANJA:
            out.append(_GIVEN_HANJA[ch])
        else:
            out.append(ch)
        i += 1
    return "".join(out)


def has_hanja(text: str) -> bool:
    """텍스트에 CJK 통합한자 (U+4E00 ~ U+9FFF) 가 포함되어 있는지."""
    return any("一" <= c <= "鿿" for c in text)
